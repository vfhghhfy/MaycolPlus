// Funci√≥n mejorada para formatear JIDs que maneja tanto @g.us como @lid
function formatJid(number) {
  if (!number) return number;
  
  // Si ya es un JID v√°lido, lo devolvemos tal como est√°
  if (number.includes('@g.us') || number.includes('@s.whatsapp.net') || number.includes('@lid')) {
    return number;
  }
  
  // Extraer solo d√≠gitos
  const digits = number.toString().replace(/[^0-9]/g, '');
  
  // Si no hay d√≠gitos, devolver el original
  if (!digits) return number;
  
  // Para n√∫meros de tel√©fono (usuarios individuales)
  if (digits.length <= 15) {
    return digits + '@s.whatsapp.net';
  }
  
  // Para identificadores m√°s largos o raros, usar @lid
  return digits + '@lid';
}

